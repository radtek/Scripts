col APPLICATION_WAIT_TIME format 99999999999999999
col CONCURRENCY_WAIT_TIME format 99999999999999999         
col CLUSTER_WAIT_TIME format 99999999999999999
col USER_IO_WAIT_TIME format 99999999999999999
col PLSQL_EXEC_TIME format 99999999999999999
col JAVA_EXEC_TIME format 99999999999999999
col TOTAL format 99999999999999999

SELECT 
	round(SUM(APPLICATION_WAIT_TIME)) AS APPLICATION_WAIT_TIME,
	round(SUM(CONCURRENCY_WAIT_TIME)) AS CONCURRENCY_WAIT_TIME,         
	round(SUM(CLUSTER_WAIT_TIME)) AS CLUSTER_WAIT_TIME,                      
	round(SUM(USER_IO_WAIT_TIME)) AS USER_IO_WAIT_TIME,                      
	round(SUM(PLSQL_EXEC_TIME)) AS PLSQL_EXEC_TIME,                        
	round(SUM(JAVA_EXEC_TIME)) JAVA_EXEC_TIME,
	round(SUM(APPLICATION_WAIT_TIME+CLUSTER_WAIT_TIME+USER_IO_WAIT_TIME+PLSQL_EXEC_TIME+JAVA_EXEC_TIME)) TOTAL
FROM v$sql;
